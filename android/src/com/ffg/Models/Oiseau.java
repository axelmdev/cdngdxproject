public class Oiseau {
    private String id;
    private String name;
    private int nbPlume;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getNbPlume() {
        return nbPlume;
    }

    public void setNbPlume(int nbPlume) {
        this.nbPlume = nbPlume;
    }
}
