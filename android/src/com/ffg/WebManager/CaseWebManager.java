    public static Case GetOneFromJson(JsonValue jsonValue){
        Case newCase = new Case();
        newCase.setMongoID(jsonValue.getString("id"));
        newCase.setUsable(jsonValue.getBoolean("usable"));
        newCase.setImg(jsonValue.getString("img"));
        newCase.setMapMongoID(jsonValue.getString("mapMongoID"));
        return newCase;
    }

    public static ArrayList<Case> GetManyFromJson(ArrayList<JsonValue> jsonValue){
        ArrayList<Case> allCase = new ArrayList<>();
        for (JsonValue characterJson : jsonValue) {
            allCase.add(GetOneFromJson(characterJson));
        }
        return allCase;
    }



    public Case GetOne(String mapID,String id) {
        Net.HttpResponseListener hrl = new Net.HttpResponseListener() {
            public void handleHttpResponse(Net.HttpResponse httpResponse) {
                String responseStringValue = httpResponse.getResultAsString();
                Gdx.app.log("WebRequest", "HTTP Response code: " + httpResponse.getStatus().getStatusCode());
                Gdx.app.log("WebRequest", "HTTP Response code: " + responseStringValue);
                Json json = new Json();
                JsonValue caseJson = json.fromJson(JsonValue.class,responseStringValue);
                CaseDAO caseDAO = new CaseDAO();
                caseDAO.Insert(GetOneFromJson(caseJson), new DBHelper(caseWebManagerContext));
            }

            public void failed(Throwable t) {
                System.out.println("Réponse PAS ok : " + t.getMessage());
                Gdx.app.log("WebRequest", "HTTP request failed");

            }

            public void cancelled() {
                System.out.println("Réponse Cancelled : ");
                Gdx.app.log("WebRequest", "HTTP request cancelled");
            }
        };

        HttpRequestBuilder requestBuilder = new HttpRequestBuilder();
        Net.HttpRequest httpRequest = requestBuilder.newRequest().method(Net.HttpMethods.GET).url(baseUrl + mapNameUrlVersion + "/" + mapID + "/" + modelNameUrlVersion + "/" + id).build();
        Gdx.net.sendHttpRequest(httpRequest, hrl);
        return null;
    }


